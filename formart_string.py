
marks = int(input('enter  the marks:'))

if 80 <= marks <= 100:
    print('A+')
elif 70 <= marks <= 80:
    print('A')
elif 60 <= marks <= 70:
    print('A-')
elif 50 <= marks <= 60:
    print('B+')
elif 40<= marks <=50:
    print('c')
elif 33<= marks <=40:
    print('d')
else:
    print('fail')



